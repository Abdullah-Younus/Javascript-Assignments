let age = 15;

if (age < 2) {
    document.writeln(`You're a baby!`);
} else if (age < 4) {
    document.writeln(`You're a toddler!`);
} else if (age < 13) {
    document.writeln(`You're a kid!`);
} else if (age < 20) {
    document.writeln(`You're a teenager!`);
} else if (age < 65) {
    document.writeln(`You're an adult!`);
} else {
    document.writeln(`You're an elder`);
}
